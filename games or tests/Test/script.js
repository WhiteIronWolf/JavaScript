let day = 3;
switch (day) {
    case 1:
        console.log("Mandag");
        break;
    case 2:
        console.log("Tirsdag");
        break;
    case 3:
        console.log("Onsdag");
        break;
    case 4:
        console.log("Torsdag");
        break;
    case 5:
        console.log("Fredag");
        break;
    case 6:
        console.log("Lørdag");
        break;
    case 7:
        console.log("Søndag");
        break;
    default:
        console.log("Invalid day")
}